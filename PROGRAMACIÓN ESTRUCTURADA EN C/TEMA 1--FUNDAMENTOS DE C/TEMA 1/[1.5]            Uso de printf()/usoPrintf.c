#include <stdio.h> ///Incluimos librerias

void main()
{
    printf("\nEl valor 92 usando el tipo de campo d es: %d \n",92);   ///Entero con signo base decimal
    printf("El valor 92 usando el tipo de campo i es: %i \n",92);   ///Entero con signo base decimal
    printf("El valor 92 usando el tipo de campo u es: %u \n",92);   ///Entero sin signo en base decimal
    printf("El valor 92 usando el tipo de campo o es: %o \n",92);   ///Entero sin signo en base octal
    printf("El valor 92 usando el tipo de campo x es: %x \n",92);   ///Entero sin signo en base hexadecimal con minusculas
    printf("El valor 92 usando el tipo de campo X es: %X \n",92);   ///Entero sin signo en base hexadecimal con MAYUSCULAS
    printf("El valor 92 usando el tipo de campo f es: %f \n",92.0); ///Real con signo
    printf("El valor 92 usando el tipo de campo e es: %e \n",92.0); ///Real con signo usando notacion e
    printf("El valor 92 usando el tipo de campo E es: %E \n",92.0); ///Real con signo usando notacion E
    printf("El valor 92 usando el tipo de campo g es: %g \n",92.0); ///Real con signo formato e o f corto
    printf("El valor 92 usando el tipo de campo G es: %G \n",92.0); ///Real con signo formato E o f corto
    printf("El valor 92 usando el tipo de campo c es: %c \n",92);   ///Caracter
    printf("El valor 92 usando el tipo de campo c es: %c \n",'9'); ///Caracter individual
    printf("El valor 92 usando el tipo de campo s es: %s \n","92"); ///Real con signo formato e o f corto

}///Fin main
